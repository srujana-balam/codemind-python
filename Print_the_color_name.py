n=input()
if (n=='V' or n=='v'):
    print("Violet")
elif (n=='I' or n=='i'):
    print("Indigo")
elif (n=='B' or n=='b'):
    print("Blue")
elif (n=='G' or n=='g'):
    print("Green")
elif (n=='Y' or n=='y'):
    print("Yellow")
elif (n=='O' or n=='o'):
    print("Orange")
elif (n=='R' or n=='r'):
    print("Red") 
else:
    print(-1)